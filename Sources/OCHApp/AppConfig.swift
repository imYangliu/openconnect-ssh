import Foundation

/// Resolves the Homebrew prefix: `/opt/homebrew` on Apple Silicon, `/usr/local`
/// on Intel. Falls back to `/opt/homebrew` when neither `och` binary is found,
/// so the defaults stay stable and remain overridable via config.yaml/gui.env.
enum BrewPrefix {
    static let path: String = {
        for prefix in ["/opt/homebrew", "/usr/local"] where
            FileManager.default.isExecutableFile(atPath: "\(prefix)/bin/och") {
            return prefix
        }
        return "/opt/homebrew"
    }()
}

struct AppConfig: Equatable {
    var vpnHost = ""
    var vpnUser = ""
    var vpnAuthGroup = ""
    var defaultHost = "och-target"
    var targetHost = ""
    var targetPort = "22"
    var targetUser = NSUserName()
    var extraRoutesText = ""
    var proxyLocalHost = "127.0.0.1"
    var proxyLocalPort = "7890"
    var proxyRemotePort = "7890"
    var ochPath = "\(BrewPrefix.path)/bin/och"
    var ochVpnPath = "\(BrewPrefix.path)/bin/och-vpn"
    var askpassPath = "\(BrewPrefix.path)/libexec/och/och-sudo-askpass.sh"

    var extraRoutes: [String] {
        extraRoutesText
            .split(whereSeparator: { $0 == "\n" || $0 == " " || $0 == "\t" || $0 == "," })
            .map(String.init)
            .filter { !$0.isEmpty }
    }
}

enum ConfigPaths {
    static let configDirectory = FileManager.default.homeDirectoryForCurrentUser
        .appendingPathComponent(".config/och", isDirectory: true)
    static let guiYAML = configDirectory.appendingPathComponent("config.yaml")
    static let guiEnv = configDirectory.appendingPathComponent("gui.env")
    static let sshConfig = FileManager.default.homeDirectoryForCurrentUser
        .appendingPathComponent(".ssh/config")
    static let managedSSHConfig = FileManager.default.homeDirectoryForCurrentUser
        .appendingPathComponent(".ssh/och.config")
}

enum YAMLConfigError: LocalizedError {
    case invalidLine(Int, String)
    case invalidSection(Int, String)

    var errorDescription: String? {
        switch self {
        case .invalidLine(let line, let value):
            return "Invalid YAML at line \(line): \(value)"
        case .invalidSection(let line, let value):
            return "Invalid YAML section at line \(line): \(value)"
        }
    }
}

enum YAMLConfigFile {
    static func load(from url: URL) throws -> AppConfig {
        let contents = try String(contentsOf: url, encoding: .utf8)
        return try parse(contents)
    }

    static func save(_ config: AppConfig, to url: URL) throws {
        try FileManager.default.createDirectory(
            at: url.deletingLastPathComponent(),
            withIntermediateDirectories: true
        )

        try render(config).write(to: url, atomically: true, encoding: .utf8)
        try FileManager.default.setAttributes([.posixPermissions: 0o600], ofItemAtPath: url.path)
    }

    static func render(_ config: AppConfig) -> String {
        var lines = [
            "# Generated by OCH. VPN password is stored in Keychain, not in this file.",
            "vpn:",
            "  host: \(quote(config.vpnHost))",
            "  user: \(quote(config.vpnUser))",
            "  auth_group: \(quote(config.vpnAuthGroup))",
            "ssh:",
            "  host: \(quote(config.defaultHost))",
            "  target_host: \(quote(config.targetHost))",
            "  user: \(quote(config.targetUser))",
            "  port: \(quote(config.targetPort))",
            "routes:"
        ]

        if config.extraRoutes.isEmpty {
            lines.append("  extra: []")
        } else {
            lines.append("  extra:")
            lines.append(contentsOf: config.extraRoutes.map { "    - \(quote($0))" })
        }

        lines.append(contentsOf: [
            "proxy:",
            "  local_host: \(quote(config.proxyLocalHost))",
            "  local_port: \(quote(config.proxyLocalPort))",
            "  remote_port: \(quote(config.proxyRemotePort))",
            "paths:",
            "  och: \(quote(config.ochPath))",
            "  och_vpn: \(quote(config.ochVpnPath))",
            "  askpass: \(quote(config.askpassPath))"
        ])

        return lines.joined(separator: "\n") + "\n"
    }

    static func parse(_ contents: String) throws -> AppConfig {
        var config = AppConfig()
        var section = ""
        var listKey: String?
        var routes: [String] = []
        var sawRoutesList = false

        for (index, rawLine) in contents.split(separator: "\n", omittingEmptySubsequences: false).enumerated() {
            let lineNumber = index + 1
            let withoutComment = stripComment(String(rawLine))
            guard !withoutComment.trimmingCharacters(in: .whitespaces).isEmpty else {
                continue
            }

            let indent = withoutComment.prefix(while: { $0 == " " }).count
            let line = withoutComment.trimmingCharacters(in: .whitespaces)

            if indent == 0 {
                guard line.hasSuffix(":") else {
                    throw YAMLConfigError.invalidSection(lineNumber, line)
                }
                section = String(line.dropLast()).trimmingCharacters(in: .whitespaces)
                listKey = nil
                continue
            }

            if indent == 2 {
                if line.hasSuffix(":") {
                    let key = String(line.dropLast()).trimmingCharacters(in: .whitespaces)
                    listKey = "\(section).\(key)"
                    if listKey == "routes.extra" {
                        routes.removeAll()
                        sawRoutesList = true
                    }
                    continue
                }

                let pair = try parsePair(line, lineNumber: lineNumber)
                if section == "routes", pair.key == "extra", pair.value == "[]" {
                    routes.removeAll()
                    sawRoutesList = true
                    listKey = nil
                    continue
                }
                apply(pair.value, section: section, key: pair.key, to: &config)
                listKey = nil
                continue
            }

            if indent == 4, line == "[]", listKey == "routes.extra" {
                routes.removeAll()
                sawRoutesList = true
                continue
            }

            if indent == 4, line.hasPrefix("- "), listKey == "routes.extra" {
                routes.append(unquote(String(line.dropFirst(2)).trimmingCharacters(in: .whitespaces)))
                sawRoutesList = true
                continue
            }

            throw YAMLConfigError.invalidLine(lineNumber, line)
        }

        if sawRoutesList {
            config.extraRoutesText = routes.joined(separator: "\n")
        }
        return config
    }

    private static func parsePair(_ line: String, lineNumber: Int) throws -> (key: String, value: String) {
        guard let colon = line.firstIndex(of: ":") else {
            throw YAMLConfigError.invalidLine(lineNumber, line)
        }
        let key = String(line[..<colon]).trimmingCharacters(in: .whitespaces)
        let value = String(line[line.index(after: colon)...]).trimmingCharacters(in: .whitespaces)
        return (key, unquote(value))
    }

    private static func apply(_ value: String, section: String, key: String, to config: inout AppConfig) {
        switch (section, key) {
        case ("vpn", "host"):
            config.vpnHost = value
        case ("vpn", "user"):
            config.vpnUser = value
        case ("vpn", "auth_group"):
            config.vpnAuthGroup = value
        case ("ssh", "host"):
            config.defaultHost = value
        case ("ssh", "target_host"):
            config.targetHost = value
        case ("ssh", "user"):
            config.targetUser = value
        case ("ssh", "port"):
            config.targetPort = value
        case ("proxy", "local_host"):
            config.proxyLocalHost = value
        case ("proxy", "local_port"):
            config.proxyLocalPort = value
        case ("proxy", "remote_port"):
            config.proxyRemotePort = value
        case ("paths", "och"):
            config.ochPath = value
        case ("paths", "och_vpn"):
            config.ochVpnPath = value
        case ("paths", "askpass"):
            config.askpassPath = value
        default:
            break
        }
    }

    private static func quote(_ value: String) -> String {
        let escaped = value
            .replacingOccurrences(of: "\\", with: "\\\\")
            .replacingOccurrences(of: "\"", with: "\\\"")
        return "\"\(escaped)\""
    }

    private static func unquote(_ value: String) -> String {
        guard value.count >= 2 else { return value }
        if value.hasPrefix("\""), value.hasSuffix("\"") {
            let body = String(value.dropFirst().dropLast())
            return body
                .replacingOccurrences(of: "\\\"", with: "\"")
                .replacingOccurrences(of: "\\\\", with: "\\")
        }
        if value.hasPrefix("'"), value.hasSuffix("'") {
            return String(value.dropFirst().dropLast())
        }
        return value
    }

    private static func stripComment(_ line: String) -> String {
        var result = ""
        var inDoubleQuote = false
        var inSingleQuote = false
        var escaping = false

        for character in line {
            if escaping {
                result.append(character)
                escaping = false
                continue
            }

            if character == "\\" {
                result.append(character)
                escaping = inDoubleQuote
                continue
            }

            if character == "\"", !inSingleQuote {
                inDoubleQuote.toggle()
            } else if character == "'", !inDoubleQuote {
                inSingleQuote.toggle()
            } else if character == "#", !inDoubleQuote, !inSingleQuote {
                break
            }

            result.append(character)
        }

        return result
    }
}

enum EnvFile {
    static func load(from url: URL) throws -> AppConfig {
        var config = AppConfig()
        guard FileManager.default.fileExists(atPath: url.path) else {
            return config
        }

        let contents = try String(contentsOf: url, encoding: .utf8)
        let values = parse(contents)
        config.vpnHost = values["VPN_HOST"] ?? config.vpnHost
        config.vpnUser = values["VPN_USER"] ?? config.vpnUser
        config.vpnAuthGroup = values["VPN_AUTHGROUP"] ?? config.vpnAuthGroup
        config.defaultHost = values["DEFAULT_HOST"] ?? config.defaultHost
        config.targetHost = values["TARGET_HOST"] ?? config.targetHost
        config.targetPort = values["TARGET_PORT"] ?? config.targetPort
        config.targetUser = values["TARGET_SSH_USER"] ?? config.targetUser
        config.extraRoutesText = (values["MACOS_EXTRA_ROUTES"] ?? "").replacingOccurrences(of: " ", with: "\n")
        config.proxyLocalHost = values["PROXY_LOCAL_HOST"] ?? config.proxyLocalHost
        config.proxyLocalPort = values["PROXY_LOCAL_PORT"] ?? config.proxyLocalPort
        config.proxyRemotePort = values["PROXY_REMOTE_PORT"] ?? config.proxyRemotePort
        config.ochPath = values["OCH_PATH"] ?? config.ochPath
        config.ochVpnPath = values["OCH_VPN_PATH"] ?? config.ochVpnPath
        config.askpassPath = values["OCH_ASKPASS_PATH"] ?? config.askpassPath
        return config
    }

    static func save(_ config: AppConfig, to url: URL) throws {
        try FileManager.default.createDirectory(
            at: url.deletingLastPathComponent(),
            withIntermediateDirectories: true
        )

        let lines = [
            "# Generated by OCH. VPN_PASSWORD is stored in Keychain, not in this file.",
            "VPN_HOST=\(quote(config.vpnHost))",
            "VPN_USER=\(quote(config.vpnUser))",
            config.vpnAuthGroup.isEmpty ? nil : "VPN_AUTHGROUP=\(quote(config.vpnAuthGroup))",
            "DEFAULT_HOST=\(quote(config.defaultHost))",
            "TARGET_HOST=\(quote(config.targetHost))",
            "TARGET_PORT=\(quote(config.targetPort))",
            "TARGET_SSH_USER=\(quote(config.targetUser))",
            config.extraRoutes.isEmpty ? nil : "MACOS_EXTRA_ROUTES=\(quote(config.extraRoutes.joined(separator: " ")))",
            "PROXY_LOCAL_HOST=\(quote(config.proxyLocalHost))",
            "PROXY_LOCAL_PORT=\(quote(config.proxyLocalPort))",
            "PROXY_REMOTE_PORT=\(quote(config.proxyRemotePort))",
            "OCH_PATH=\(quote(config.ochPath))",
            "OCH_VPN_PATH=\(quote(config.ochVpnPath))",
            "OCH_ASKPASS_PATH=\(quote(config.askpassPath))"
        ].compactMap { $0 }.joined(separator: "\n") + "\n"

        try lines.write(to: url, atomically: true, encoding: .utf8)
        try FileManager.default.setAttributes([.posixPermissions: 0o600], ofItemAtPath: url.path)
    }

    private static func parse(_ contents: String) -> [String: String] {
        var result: [String: String] = [:]
        for rawLine in contents.split(separator: "\n", omittingEmptySubsequences: false) {
            let line = rawLine.trimmingCharacters(in: .whitespaces)
            guard !line.isEmpty, !line.hasPrefix("#"), let equals = line.firstIndex(of: "=") else {
                continue
            }
            let key = String(line[..<equals]).trimmingCharacters(in: .whitespaces)
            var value = String(line[line.index(after: equals)...]).trimmingCharacters(in: .whitespaces)
            if value.hasPrefix("\""), value.hasSuffix("\""), value.count >= 2 {
                value.removeFirst()
                value.removeLast()
            }
            result[key] = value
        }
        return result
    }

    private static func quote(_ value: String) -> String {
        let escaped = value
            .replacingOccurrences(of: "\\", with: "\\\\")
            .replacingOccurrences(of: "\"", with: "\\\"")
            .replacingOccurrences(of: "$", with: "\\$")
            .replacingOccurrences(of: "`", with: "\\`")
        return "\"\(escaped)\""
    }
}
